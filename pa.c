#include "push_swap.h"

void    pa(t_Node **a, t_Node **b)
{
    t_Node  *temp;

    if (*b == NULL)
        return ;
    temp = *b;
    *b = (*b)->next;
    temp->next = *a;
    *a = temp;
    write(1, "pa\n", 3);
}